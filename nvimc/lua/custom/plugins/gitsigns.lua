return { -- Adds git related signs to the gutter, as well as utilities for managing changes
  'lewis6991/gitsigns.nvim',
  opts = function()
    vim.keymap.set('n', '<leader>gb', '<cmd>gitsigns blame_line<CR>', { desc = '[G]it [B]lame' })
    return {
      signs = {
        add = { text = '+' },
        change = { text = '~' },
        delete = { text = '_' },
        topdelete = { text = 'â€¾' },
        changedelete = { text = '~' },
      },
    }
  end,
}
